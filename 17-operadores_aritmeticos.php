<?php 
    //Operadores aritiméticos:
    /*
    -Adição: +;
    -Subtração: -;
    -Divisão: /;
    -Multiplicação: *;
    -Resto da divisão ou módulo: %;
    -Exponenciação: **;    
    */

    $num1 = 4;
    $num2 = 2;
    //Adição:
    echo "$num1 + $num2 = ".$num1+$num2;
    echo "<hr>";

    //subtração:
    echo "$num1 - $num2 = ".$num1-$num2;
    echo "<hr>";

    //Divisão:
    echo "$num1 / $num2 = ".$num1/$num2;
    echo "<hr>";

    //Multiplicação:
    echo "$num1 x $num2 = ".$num1*$num2;
    echo "<hr>";

    //Resto da divisão ou módulo:
    echo "$num1 % $num2 = ".$num1%$num2;
    echo "<hr>";

    //Exponenciação:
    echo "$num1 ^ $num2 = ".$num1**$num2;
    echo "<hr>";
?>